public class BasicVariable3 {
	
	public static void main(String[] args) {
		
		String kalimat1 = "9";
		int angka1 = Integer.parseInt(kalimat1);
		
		System.out.println(angka1);
		
		int angka2 = 10;
		String kalimat2 = angka2 +"";
		
		System.out.println(kalimat2);
		
		String kalimat3 = Integer.toString(angka2);
		
		System.out.println(kalimat3);
		
	}

}